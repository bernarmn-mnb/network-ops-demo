#!/usr/bin/env node
/**
 * Generate Complete EUI Icon Cache
 * 
 * This script imports EVERY icon from @elastic/eui to ensure
 * no icon is ever missing. For demos where bundle size doesn't matter.
 * 
 * Usage: node scripts/generate-all-icons.cjs
 */

const fs = require('fs');
const path = require('path');

// Find the EUI icons directory
const euiIconsPath = path.join(__dirname, '../node_modules/@elastic/eui/es/components/icon/assets');

if (!fs.existsSync(euiIconsPath)) {
  console.error('❌ EUI icons directory not found. Run `yarn install` first.');
  process.exit(1);
}

// Get all icon files
const iconFiles = fs.readdirSync(euiIconsPath)
  .filter(f => f.endsWith('.js') && !f.startsWith('_'))
  .map(f => f.replace('.js', ''))
  .sort();

console.log(`Found ${iconFiles.length} EUI icons`);

// Reserved words that need special handling
const reservedWords = ['export', 'import', 'function', 'package', 'default', 'class', 'return'];

// Convert snake_case to camelCase
function toCamelCase(str) {
  return str.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
}

// Track used variable names to avoid duplicates
const usedVarNames = new Set();

// Generate unique variable name
function toVarName(iconName) {
  let baseName = toCamelCase(iconName);
  
  // Handle reserved words
  if (reservedWords.includes(baseName) || reservedWords.includes(iconName)) {
    baseName = `${baseName}Icon`;
  }
  
  // Ensure uniqueness
  let varName = baseName;
  let counter = 1;
  while (usedVarNames.has(varName)) {
    varName = `${baseName}${counter}`;
    counter++;
  }
  usedVarNames.add(varName);
  
  return varName;
}

// Build imports and track mappings
const imports = [];
const iconVarMap = new Map(); // iconFileName -> varName

iconFiles.forEach(iconName => {
  const varName = toVarName(iconName);
  iconVarMap.set(iconName, varName);
  imports.push(`import { icon as ${varName} } from '@elastic/eui/es/components/icon/assets/${iconName}'`);
});

// Build cache entries (both snake_case and camelCase)
const cacheEntries = [];
const seenCacheKeys = new Set();

iconFiles.forEach(iconName => {
  const varName = iconVarMap.get(iconName);
  const camelName = toCamelCase(iconName);
  
  // Add the original filename as key
  if (!seenCacheKeys.has(iconName)) {
    cacheEntries.push(`  '${iconName}': ${varName}`);
    seenCacheKeys.add(iconName);
  }
  
  // Add camelCase version if different and not already added
  if (camelName !== iconName && !seenCacheKeys.has(camelName)) {
    cacheEntries.push(`  ${camelName}: ${varName}`);
    seenCacheKeys.add(camelName);
  }
});

// Generate the file
const output = `/**
 * EUI Icon Cache - COMPLETE EDITION
 * 
 * AUTO-GENERATED by scripts/generate-all-icons.cjs
 * DO NOT EDIT MANUALLY
 * 
 * This file imports ALL ${iconFiles.length} EUI icons to ensure no icon is ever missing.
 * Bundle size impact: ~200-300KB (acceptable for demos)
 * 
 * Regenerate with: node scripts/generate-all-icons.cjs
 */
import { appendIconComponentCache } from '@elastic/eui/es/components/icon/icon'

${imports.join('\n')}

appendIconComponentCache({
${cacheEntries.join(',\n')}
})

// All ${iconFiles.length} EUI icons are now available
`;

// Write the file
const outputPath = path.join(__dirname, '../src/iconCache.ts');
fs.writeFileSync(outputPath, output);

console.log(`✅ Generated ${outputPath}`);
console.log(`   - ${iconFiles.length} icons imported`);
console.log(`   - ${cacheEntries.length} cache entries (including camelCase aliases)`);
